package com.gn.practice2;

public class Practice02 {
	public static void main(String[] args) {
		//변수명이 isStudent인 논리형 변수
		boolean isStudent = true;
		System.out.println(isStudent);
		//오늘 받은 학점(알파벳) char 변수 grade
		char grade = 'A';
		System.out.println(grade);
		//철수가 먹은 사탕의 개수 byte 변수 candy
		byte candy = 2;
		System.out.println(candy);
		//철수가 걸은 걸음 short 변수 walk
		short walk = 2000;
		System.out.println(walk);
		//개강날 이후 지난 날의 일수 int 변수 day
		int day = 3;
		System.out.println(day);
		//종강하기 까지 남은 날의 일수 long 변수 future
		long future = 180;
		System.out.println(future);
		//철수의 키 float 변수 height
		float height = 179.86f;
		System.out.println(height);
		//철수의 예상 학점 double 변수 score
		double score = 3.8;
		System.out.println(score);
		
	}

}
